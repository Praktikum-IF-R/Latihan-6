package Filkom;

public class Fakultas {
    private String fakultas, rektor, univ;
    
    public Fakultas(String f, String r, String u){
        this.fakultas = f;
        this.rektor = r;
        this.univ = u;
    }
    public String getFakultas(){
        return fakultas;
    }
    public String getRektor(){
        return rektor;
    }
    public String getUniv(){
        return univ;
    }
    
    
}
