
package Bab6;

public class Fakultas {
     public String NamaFak;
     public String Universitas;
     public String Rektor;

    public String getNamaFak() {
        return NamaFak;
    }

    public String getUniversitas() {
        return Universitas;
    }

    public String getRektor() {
        return Rektor;
    }

    public void setNamaFak(String NamaFak) {
        this.NamaFak = NamaFak;
    }

    public void setUniversitas(String Universitas) {
        this.Universitas = Universitas;
    }

    public void setRektor(String Rektor) {
        this.Rektor = Rektor;
    }
     
    
}

